 * Base for hexadecimal numbers.
 */
const val HEXADECIMAL = 16

/**
 * Constant used for shuffling.
 */
const val SHUFFLE = 0xFFFF

/**
 * Modulus constant.
 */
const val MOD = 1_000_000_007

